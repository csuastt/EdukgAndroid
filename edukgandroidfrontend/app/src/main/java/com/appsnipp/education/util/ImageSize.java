package com.appsnipp.education.util;

/***
 * This is a file which is used for determining the image size in chat activity
 * @author Shuning Zhang
 * @version 1.0
 */

public class ImageSize {

    private int width;
    private int height;
    public int getWidth() {
        return width;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }


}
